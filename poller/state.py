"""
グローバル可変状態の一元管理

スレッド間で共有される可変状態をロック付きで管理する。
"""

import threading
from typing import Any

from poller.config import MEMBERS

# =============================================================================
#  会話チェーン（AI 同士の往復カウンタ、ルームごと）
# =============================================================================

conversation_chains: dict[str, dict[str, Any]] = {}
chain_lock = threading.Lock()

# =============================================================================
#  シャットダウン
# =============================================================================

shutdown_requested: bool = False

# =============================================================================
#  CLI モード: 実行中の子プロセス追跡
# =============================================================================

active_processes: list[Any] = []
process_lock = threading.Lock()

# =============================================================================
#  セッション状態（各メンバーの AI 実行状況）
# =============================================================================

session_states: dict[str, dict[str, Any]] = {
    key: {"status": "idle", "started": None, "room_id": "", "model": ""}
    for key in MEMBERS
}
session_lock = threading.Lock()

# =============================================================================
#  連投防止（メンバーごとの最終発言時刻）
# =============================================================================

last_reply_time: dict[str, float] = {}
reply_time_lock = threading.Lock()

# =============================================================================
#  メンバー排他ロック（同一メンバーの同時実行を防止）
# =============================================================================

member_locks: dict[str, threading.Lock] = {key: threading.Lock() for key in MEMBERS}

# =============================================================================
#  /talk 対話セッション
# =============================================================================

talk_session: dict[str, Any] = {}
talk_session_lock = threading.Lock()
